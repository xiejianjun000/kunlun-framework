def route_question(question: str) -> tuple:
    """根据问题类型路由到合适的Agent"""
    q = question.lower()
    
    if any(kw in q for kw in ['法律', '法规', '条例', '条款', '排污许可', '环境保护法']):
        return "law_expert", LAW_KNOWLEDGE
    elif any(kw in q for kw in ['标准', '规范', '监测', '排放', '技术要求', '2026']):
        return "standard_expert", STANDARD_KNOWLEDGE
    elif any(kw in q for kw in ['案例', '赔偿', '磋商', '损害', '公益诉讼']):
        return "case_expert", CASE_KNOWLEDGE
    elif any(kw in q for kw in ['基准', '鉴定', '评估', '推导', '计算']):
        return "benchmark_expert", BENCHMARK_KNOWLEDGE
    else:
        return "root_agent", "请问您想咨询哪方面的生态环境问题？法律、法规、标准、案例或基准？"

def test_agent(question: str):
    """测试Agent响应"""
    agent_name, response = route_question(question)
    print(f"\n{'='*60}")
    print(f"📌 用户问题: {question}")
    print(f"🤖 路由Agent: {agent_name}")
    print(f"{'='*60}")
    print(f"\n📖 回答内容:\n{response}\n")

