#!/usr/bin/env bash
# story.sh — 故事创作工具（真实生成版）
# Usage: bash story.sh <command> [args...]
# Commands: structure, character, plot, worldbuild, dialogue, random
set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true
INPUT="$*"

# ── 随机元素库 ──
FIRST_NAMES=("林晨" "苏瑶" "陈默" "方远" "叶知秋" "周星河" "沈墨" "白露" "楚云" "萧然" "顾惜" "宋清" "江流" "唐风" "赵夜")
TRAITS=("冷静理智但内心温柔" "热情冲动偶尔莽撞" "外冷内热不善言辞" "聪明机警善于伪装" "乐观开朗但有伤痛" "沉默寡言但观察力惊人" "嘴硬心软死不承认" "表面佛系内心倔强" "话痨但每句都有道理" "社恐但能力超群")
MOTIVATIONS=("找到失踪的亲人" "复仇但逐渐放下" "保护重要的人" "揭开一个秘密" "赎罪与自我救赎" "追求自由" "证明自己的价值" "为了一个承诺" "逃离过去" "改变不公的规则")
CONFLICTS=("信念与现实的冲突" "亲情与正义的抉择" "个人利益vs集体利益" "过去的创伤vs未来的可能" "自由与责任" "真相与谎言" "爱与放手" "理想与妥协" "复仇与宽恕" "独立与归属")
SETTINGS=("赛博朋克城市2077" "古代江湖武林" "现代都市悬疑" "末日废土" "星际航行" "民国乱世" "校园青春" "平行世界" "深海研究站" "虚拟现实游戏世界")
PLOT_TWISTS=("受信任的人其实是敌人" "主角自己就是幕后黑手（失忆）" "两个世界线的交汇" "死去的人其实还活着" "任务的真正目标不是看起来那样" "最大的敌人是主角自己的另一面" "所谓的真相只是更大谎言的一部分" "帮助者的真实动机令人震惊" "时间循环——主角已经经历过很多次" "被认为无关的小角色是关键")

random_pick() {
  local -n arr=$1
  local idx=$((RANDOM % ${#arr[@]}))
  echo "${arr[$idx]}"
}

# ── 故事结构模板 ──
generate_structure() {
  local genre="${1:-科幻}"
  local acts="${2:-3}"

  echo "# 📖 故事结构模板"
  echo ""
  echo "> 类型: ${genre}"
  echo "> 结构: ${acts}幕式"
  echo "> 生成时间: $(date '+%Y-%m-%d %H:%M')"
  echo ""

  case "$acts" in
    3)
      cat <<EOF
## 三幕式结构 (经典好莱坞)

### 📊 节奏比例
| 幕 | 占比 | 字数(短篇5000字) | 字数(中篇20000字) |
|----|------|-----------------|------------------|
| 第一幕: 建置 | 25% | ~1,250字 | ~5,000字 |
| 第二幕: 对抗 | 50% | ~2,500字 | ~10,000字 |
| 第三幕: 解决 | 25% | ~1,250字 | ~5,000字 |

---

### 第一幕: 建置 (Setup) — 前25%

#### 开场 (0-10%)
- **日常世界**: 主角的平凡生活，但暗示不满/缺失
- **核心问题**: 主角最大的weakness是什么？
- **氛围建立**: 通过细节（天气/环境/习惯）定调

#### 催化事件 (10-12%)
- **打破平衡**: 一个不可忽视的事件
- 例: 收到神秘信件 / 遇到关键人物 / 发现异常
- ⚠️ 这必须是不可逆的！

#### 犹豫/辩论 (12-25%)
- 主角尝试拒绝冒险
- 内心挣扎: 恐惧 vs 渴望
- 导师/催化角色的推动

#### 第一转折点 (25%)
- **跨越门槛**: 主角做出决定，不可回头
- 故事正式开始

---

### 第二幕: 对抗 (Confrontation) — 中间50%

#### 新世界探索 (25-37%)
- 新规则、新盟友、新敌人
- 主角的能力被测试
- B故事线启动（通常是感情线）

#### 中点 (50%)
- **假胜利或假失败**
- 赌注升级: 从个人→关乎更多人
- 重要信息揭示

#### 坏人逼近 (50-75%)
- 反派变得更强大/更具威胁
- 盟友出现问题/背叛
- 主角的weakness开始成为障碍
- 时间压力增大

#### 一切失去/至暗时刻 (75%)
- **最低点**: 主角失去一切
- 导师/重要人物死亡或离开
- 看起来完全没有希望

---

### 第三幕: 解决 (Resolution) — 后25%

#### 顿悟 (75-80%)
- 主角理解了自己的weakness
- A故事线和B故事线交汇
- 找到新的力量/方法

#### 最终对决 (80-95%)
- 高潮: 与终极挑战正面对抗
- 主角运用成长后的能力
- **主题的最终表达**

#### 新平衡 (95-100%)
- 世界恢复（但已不同）
- 主角的改变得到体现
- 留下余韵/开放空间

---

### 🎯 ${genre}类型特有元素

$(case "$genre" in
  科幻|sci-fi)
    echo "- 科技设定需在前20%建立清楚"
    echo "- 科技难题应与角色成长相呼应"
    echo "- 高潮中技术和人性的结合"
    echo "- 结局要回应\"技术带来了什么\"的哲学问题"
    ;;
  悬疑|推理|mystery)
    echo "- 线索在前50%全部埋下"
    echo "- 误导(Red Herring)至少2-3个"
    echo "- 中点的假揭示是为了更大的秘密"
    echo "- 结局要让读者\"恍然大悟\"同时\"情理之中\""
    ;;
  奇幻|fantasy)
    echo "- 魔法体系的规则在前30%说明"
    echo "- 世界观通过角色视角展示(Show don't tell)"
    echo "- 力量的代价与主题呼应"
    echo "- 避免Deus Ex Machina——力量必须有限制"
    ;;
  爱情|romance)
    echo "- 两人相遇(Meet-Cute)在催化事件"
    echo "- 中点是关系进展/亲密时刻"
    echo "- 至暗时刻是两人的误解/分离"
    echo "- 高潮是坦诚相对+克服障碍"
    ;;
  *)
    echo "- 类型惯例需要在前20%建立"
    echo "- 符合类型读者的期待但有新意"
    echo "- 高潮满足类型承诺"
    ;;
esac)
EOF
      ;;
    5)
      cat <<'EOF'
## 五幕式结构 (莎士比亚/电视剧)

| 幕 | 名称 | 占比 | 功能 |
|----|------|------|------|
| 第一幕 | 引子 | 10% | 世界建立+催化事件 |
| 第二幕 | 上升 | 25% | 冲突发展+盟友集结 |
| 第三幕 | 高潮 | 30% | 最大的冲突+转折 |
| 第四幕 | 下降 | 20% | 后果+新冲突 |
| 第五幕 | 结局 | 15% | 解决+新秩序 |

### 适用场景
- 复杂多线叙事
- 电视剧/连续剧
- 史诗/群像故事
EOF
      ;;
    *)
      echo "支持的结构: 3幕 / 5幕"
      ;;
  esac
}

# ── 角色生成 ──
generate_character() {
  local role="${1:-主角}"
  local genre="${2:-现代}"

  local name=$(random_pick FIRST_NAMES)
  local trait=$(random_pick TRAITS)
  local motivation=$(random_pick MOTIVATIONS)
  local conflict=$(random_pick CONFLICTS)
  local flaw=$(echo "过度自信|过于敏感|控制欲强|害怕承诺|逃避问题|完美主义|不信任他人|自我牺牲倾向|报复心重|不善拒绝" | tr '|' '\n' | shuf -n1)
  local strength=$(echo "超强直觉|冷静决策|共情能力|领导魅力|学习能力|坚韧意志|创造力|洞察力|行动力|感染力" | tr '|' '\n' | shuf -n1)

  local age=$((18 + RANDOM % 40))
  local blood=$(echo "A B O AB" | tr ' ' '\n' | shuf -n1)
  local mbti=$(echo "INTJ INFP ENTJ ENFP ISTP ISFJ ESTP ENTP" | tr ' ' '\n' | shuf -n1)

  cat <<EOF
# 🧑 角色卡 — ${name}

> 类型: ${role}
> 生成时间: $(date '+%Y-%m-%d %H:%M')

## 基础信息

| 属性 | 值 |
|------|-----|
| 姓名 | ${name} |
| 年龄 | ${age}岁 |
| 性别 | [自定义] |
| 职业 | [自定义] |
| MBTI | ${mbti} |
| 血型 | ${blood}型 |

## 性格设定

- **核心特质**: ${trait}
- **最大优势**: ${strength}
- **致命缺陷**: ${flaw}
- **口头禅**: "[根据性格设定]"

## 动机与冲突

- **核心动机**: ${motivation}
- **内在冲突**: ${conflict}
- **最怕什么**: [与动机相关的恐惧]
- **愿意为什么牺牲**: [与冲突相关的选择]

## 角色弧光 (Character Arc)

\`\`\`
起点状态 ──→ 催化事件 ──→ 挣扎期 ──→ 顿悟 ──→ 终点状态
   │              │            │         │          │
 ${flaw}      [外部冲击]    反复犯错   理解真正   ${strength}
 占主导                     的选择     的力量     占主导
\`\`\`

### 弧光阶段详解

| 阶段 | 信念 | 行为表现 | 关键场景 |
|------|------|---------|---------|
| 起点 | "${flaw}是保护自己的方式" | [具体行为] | [场景] |
| 测试 | "也许我错了…不，没错" | 犹豫+退缩 | [场景] |
| 转折 | "我必须改变" | 尝试新方式 | [场景] |
| 终点 | "${strength}才是真正的力量" | 展现成长 | [场景] |

## 关系网络

| 角色 | 关系 | 冲突点 | 影响 |
|------|------|--------|------|
| [角色B] | [关系] | [矛盾] | [推动/阻碍] |
| [角色C] | [关系] | [矛盾] | [推动/阻碍] |
| [角色D] | [关系] | [矛盾] | [推动/阻碍] |

## 背景故事（1段）

> ${name}，${age}岁，${trait}。在[成长环境]中长大，因为[关键事件]，形成了${flaw}的性格。但在${motivation}的过程中，[TA]的${strength}逐渐显露。[TA]最大的挑战是面对${conflict}——当那一刻来临时，[TA]会如何选择？

## 🎲 写作提示

- **冲突场景**: 当${motivation}和${flaw}发生冲突时——例如[具体场景]
- **成长时刻**: 当${name}第一次战胜${flaw}时
- **关系测试**: 当[关系人]触发${conflict}时
EOF
}

# ── 情节生成器 ──
generate_plot() {
  local genre="${1:-}"

  local setting=$(random_pick SETTINGS)
  local twist=$(random_pick PLOT_TWISTS)
  local protagonist=$(random_pick FIRST_NAMES)
  local antagonist=$(random_pick FIRST_NAMES)
  local motivation=$(random_pick MOTIVATIONS)
  local conflict=$(random_pick CONFLICTS)

  # 确保主角和反派不同名
  while [[ "$protagonist" == "$antagonist" ]]; do
    antagonist=$(random_pick FIRST_NAMES)
  done

  local macguffin=$(echo "一枚古老的钥匙|一份加密文件|一段失落的记忆|一个未来预言|一个量子装置|一封遗书|一个AI|一张藏宝图|一段录音|一个频率" | tr '|' '\n' | shuf -n1)

  cat <<EOF
# 🎬 情节种子 #$(shuf -i 1000-9999 -n1)

> 生成时间: $(date '+%Y-%m-%d %H:%M')
> ⚡ 不喜欢？重新运行获取新情节！

## 核心设定

| 元素 | 内容 |
|------|------|
| 🌍 背景 | ${setting} |
| 👤 主角 | ${protagonist} |
| 👹 对手 | ${antagonist} |
| 🎯 动机 | ${motivation} |
| ⚡ 核心冲突 | ${conflict} |
| 🔮 麦格芬 | ${macguffin} |
| 💥 反转 | ${twist} |

## 情节大纲

### 开场
${protagonist}在${setting}中过着[看似平静]的生活。但[TA]的内心深处，一直被${motivation}所驱动。一天，[TA]发现了${macguffin}——这改变了一切。

### 发展
为了${motivation}，${protagonist}不得不与${antagonist}正面交锋。在这个过程中，${conflict}不断加剧。每一次选择都让[TA]更接近真相，但也更危险。

### 高潮
当一切似乎要结束时——${twist}。所有人都被震惊了，包括${protagonist}自己。[TA]必须重新审视一切，做出最终的选择。

### 结局
[开放/闭合] — ${protagonist}最终[?]，而${setting}的世界也因此[改变/回归]。但故事真正结束了吗？

## 🎲 可选附加冲突
1. $(random_pick CONFLICTS)
2. $(random_pick CONFLICTS)
3. $(random_pick CONFLICTS)

## 💡 写作挑战
- 用第一人称重写这个故事
- 从${antagonist}的视角写
- 把类型改为${SETTINGS[$((RANDOM % ${#SETTINGS[@]}))]%%的*}试试
EOF
}

# ── 世界观构建 ──
generate_worldbuild() {
  local setting="${1:-科幻都市}"

  cat <<EOF
# 🌍 世界观构建模板 — ${setting}

> 生成时间: $(date '+%Y-%m-%d %H:%M')

## 一、物理世界

### 地理
| 元素 | 设定 |
|------|------|
| 地形 | [描述] |
| 气候 | [描述] |
| 资源 | [稀缺/丰富的关键资源] |
| 特殊地点 | [3-5个重要场所] |

### 科技/魔法体系
| 维度 | 设定 |
|------|------|
| 能量来源 | [什么驱动这个世界] |
| 核心规则 | [能做什么/不能做什么] |
| 代价 | [使用的成本] |
| 限制 | [硬限制是什么] |
| 禁忌 | [不能触碰的底线] |

## 二、社会结构

### 权力层级
\`\`\`
  [统治者/AI/神]
       │
  [管理层/贵族]
       │
  [中间层/公民]
       │
  [底层/边缘人]
\`\`\`

### 阵营
| 阵营 | 信念 | 目标 | 与主角关系 |
|------|------|------|-----------|
| [阵营A] | [核心价值观] | [追求什么] | [盟友/中立/敌对] |
| [阵营B] | [核心价值观] | [追求什么] | [盟友/中立/敌对] |
| [阵营C] | [核心价值观] | [追求什么] | [盟友/中立/敌对] |

## 三、历史时间线

| 时间 | 事件 | 影响 |
|------|------|------|
| 远古 | [起源事件] | 奠定世界基础 |
| 200年前 | [重大变革] | 形成当前秩序 |
| 50年前 | [关键战争/灾难] | 创伤和分裂 |
| 10年前 | [最近事件] | 当前冲突的直接原因 |
| 现在 | 故事开始 | — |

## 四、文化细节（让世界"活"起来）

- **食物**: [特色食物是什么]
- **语言**: [有没有特殊用语/俚语]
- **节日**: [庆祝什么/纪念什么]
- **禁忌**: [不能做什么]
- **货币**: [什么有价值]
- **艺术**: [人们创造什么]

## 五、写作守则

1. ✅ 通过角色的视角展示世界(Show don't tell)
2. ✅ 规则一旦建立不能随意打破
3. ✅ 历史影响当下——角色会引用/讨论历史事件
4. ❌ 避免一次性信息倾泻(Info dump)
5. ❌ 避免完美乌托邦——每个好处都有代价
EOF
}

# ── 帮助 ──
show_help() {
  cat <<'HELP'
📖 故事创作工具 — story.sh

用法: bash story.sh <command> [args...]

命令:
  structure [类型] [幕数]
        → 故事结构模板（含节奏比例+关键节点）
        类型: 科幻/悬疑/奇幻/爱情  幕数: 3/5
  character [角色类型] [类型]
        → 随机角色卡（性格/动机/弧光/关系网）
  plot [类型]
        → 随机情节种子（含反转+冲突+写作挑战）
  worldbuild [设定]
        → 世界观构建模板（地理/社会/历史/文化）
  help  → 显示帮助

示例:
  bash story.sh structure 科幻 3
  bash story.sh character 主角
  bash story.sh character 反派 奇幻
  bash story.sh plot
  bash story.sh worldbuild "赛博朋克"

💡 特色:
  - 随机角色生成（15+名字×10性格×10动机）
  - 情节种子含反转（10种反转模式）
  - 三幕/五幕结构含节奏百分比
  - 角色弧光可视化
  - 每次运行结果不同——灵感永不枯竭！
HELP
}

case "$CMD" in
  structure)
    IFS=' ' read -ra A <<< "$INPUT"
    generate_structure "${A[0]:-科幻}" "${A[1]:-3}"
    ;;
  character)
    IFS=' ' read -ra A <<< "$INPUT"
    generate_character "${A[0]:-主角}" "${A[1]:-现代}"
    ;;
  plot)     generate_plot "$INPUT" ;;
  worldbuild) generate_worldbuild "${INPUT:-科幻都市}" ;;
  help|*)   show_help ;;
esac
